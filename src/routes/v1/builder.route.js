const express = require('express');
const auth = require('../../middlewares/auth');
const validate = require('../../middlewares/validate');
const { builderController, employeeController } = require('../../controllers');
const { builderValidation } = require('../../validations');
const { checkUserStatusMiddleware } = require('../../middlewares/status.check');
const router = express.Router();

router
  .route('/builder-details')
  .get(auth('getBuilder'), builderController.getBuilderAgent)
  .patch(auth('updateBuilder'), checkUserStatusMiddleware, builderController.updateBuilder)

router
  .route('/get-employees')
  .get(auth('getBuilderEmployees'), employeeController.getEmployeesByBuilderAgent)

module.exports = router;

